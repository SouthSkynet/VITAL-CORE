    private void cargarCitasAgendadas() {
        DefaultTableModel modelo = (DefaultTableModel) tablaCitasAgendadas.getModel();
        modelo.setRowCount(0);
        for (CitaConcreta cita : citasAgendadas) {
            modelo.addRow(new Object[]{
                cita.getIdCita(),
                cita.getFecha(),
                cita.getHora(),
                cita.getNombrePaciente(),
                cita.getMotivo(),
                cita.getEstado()
            });
        }
    }
    
    private void cargarCitasAtendidas() {
        DefaultTableModel modelo = (DefaultTableModel) tablaCitasAtendidas.getModel();
        modelo.setRowCount(0);
        for (CitaConcreta cita : citasAtendidas) {
            modelo.addRow(new Object[]{
                cita.getIdCita(),
                cita.getFecha(),
                cita.getHora(),
                cita.getNombrePaciente(),
                cita.getMotivo(),
                cita.getEstado()
            });
        }
    }
    
    private void atenderCita() {
        int fila = tablaCitasAgendadas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una cita para atender");
            return;
        }
        
        String idCita = (String) tablaCitasAgendadas.getValueAt(fila, 0);
        String paciente = (String) tablaCitasAgendadas.getValueAt(fila, 3);
        
        frmAtenderCita ventana = new frmAtenderCita(idCita, paciente);
        ventana.setVisible(true);
        ventana.setLocationRelativeTo(this);
        
        // Mover cita a atendidas
        CitaConcreta cita = citasAgendadas.get(fila);
        cita.setEstado("Atendida");
        citasAtendidas.add(cita);
        citasAgendadas.remove(fila);
        
        cargarCitasAgendadas();
        cargarCitasAtendidas();
    }
    
    private void verDetalles() {
        int fila = tablaCitasAtendidas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una cita para ver detalles");
            return;
        }
        
        CitaConcreta cita = citasAtendidas.get(fila);
        String detalles = String.format(
            "DETALLES DE LA CITA\n\n" +
            "ID: %s\n" +
            "Paciente: %s\n" +
            "Fecha: %s\n" +
            "Hora: %s\n" +
            "Motivo: %s\n" +
            "Estado: %s",
            cita.getIdCita(), cita.getNombrePaciente(), cita.getFecha(), 
            cita.getHora(), cita.getMotivo(), cita.getEstado()
        );
        
        JOptionPane.showMessageDialog(this, detalles, "Detalles", JOptionPane.INFORMATION_MESSAGE);
    }
    
